package com.quarkton;

public class CommonConstant {
    private CommonConstant() {}
    public static final long BYTE = 8L;//unit: bit
    public static final long KILOBYTE = 1024L;//unit: byte
    public static final long MEGABYTE = 1024L*KILOBYTE;//unit: byte
    public static final long GIGABYTE = 1024L*MEGABYTE;//unit: byte
}
